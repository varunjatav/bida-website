<script>
    document.addEventListener('DOMContentLoaded', function() {
        const currentPath = window.location.pathname.split("/").pop().split("?")[0];
        const navLinks = document.querySelectorAll('.nav-link');
        const dropdownItems = document.querySelectorAll('.dropdown-item');

        dropdownItems.forEach(item => {
            const itemPath = item.getAttribute('href').split("/").pop().split("?")[0];
            if (currentPath === itemPath) {
                item.classList.add('active');
                const parentDropdown = item.closest('.nav-item.dropdown');
                if (parentDropdown) {
                    parentDropdown.querySelector('.nav-link').classList.add('active');
                }
            }
        });

        navLinks.forEach(link => {
            const linkPath = link.getAttribute('href').split("/").pop().split("?")[0];
            if (currentPath === linkPath && link.getAttribute('href') !== 'https://innobles.com/resource/') {
                link.classList.add('active');
            }
        });
    });
</script>
